# 1. WHILE LOOP
number = 0
while number <= 5:
    print(number)
    number = number + 1

i = 0
while range(i, 10):
    print(i)
    i = i + 1

y = 10
while y > 4:
    print(y)
    y = y - 1

count = 20
while count >= 15:
    print(count)
    count += -1

# 2. FOR IN LOOP
for x in range(100,50,-1):
    print(x)
